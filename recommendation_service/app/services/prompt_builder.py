from ..schemas.recommendation import RecommendationMode, SourceBook


class PromptBuilder:
    @staticmethod
    def build_prompt(
        book: SourceBook,
        mode: RecommendationMode,
        count: int,
    ) -> str:
        candidate_count = max(count * 2, count + 5)

        book_info = f"""
Исходная книга:
- Название: {book.title}
- Автор: {book.author or "не указан"}
- Жанр: {book.genre or "не указан"}
- Описание/аннотация: {book.description or "не указано"}
"""

        if mode == RecommendationMode.SAME_GENRE:
            task = """
Режим подбора: same_genre.

Подбери книги в том же или максимально близком жанре.

Основной критерий:
- жанровое сходство.

Дополнительно учитывай:
- поджанр;
- целевую аудиторию;
- масштаб истории;
- тип конфликта;
- темп повествования;
- общую читательскую аудиторию.
"""
            similarity_type = "genre"

        elif mode == RecommendationMode.AUTHOR_STYLE:
            task = """
Режим подбора: author_style.

Подбери книги, похожие по авторскому стилю.

Учитывай:
- манеру повествования;
- язык и тон;
- темп;
- композицию;
- уровень психологизма;
- эмоциональность;
- тип персонажей;
- способ раскрытия конфликта;
- степень литературности текста;
- ощущение от чтения.
"""
            similarity_type = "author_style"

        else:
            task = """
Режим подбора: plot_atmosphere.

Подбери книги с похожим сюжетом, атмосферой и эмоциональным ощущением.

Учитывай:
- центральные темы;
- настроение;
- динамику сюжета;
- сеттинг;
- тип главного конфликта;
- характер развития персонажей;
- эмоциональное впечатление;
- степень драматичности;
- наличие интриг, тайн, приключений, философских или психологических мотивов;
- общий читательский опыт после прочтения.
"""
            similarity_type = "plot_atmosphere"

        return f"""
Ты — книжная рекомендательная система приложения Bookish Corner.

Твоя задача — подобрать книги, которые действительно могут понравиться читателю исходной книги.

{book_info}

{task}

КРИТИЧЕСКИ ВАЖНЫЕ ПРАВИЛА:
1. Рекомендуй только реально существующие книги.
2. Не выдумывай авторов, названия, серии и факты.
3. Не путай автора книги с автором другой книги или серии.
4. Если ты не уверен в точном названии или авторе — не включай книгу в ответ.
5. Не рекомендуй исходную книгу.
6. Не рекомендуй всю серию целиком, если можно указать конкретную книгу.
7. В поле title должна быть конкретная книга, а не название цикла, вселенной или жанра.
8. В поле author должен быть настоящий автор именно этой книги.
9. Не добавляй книгу только потому, что она популярная.
10. Каждая рекомендация должна быть логически связана с исходной книгой.
11. Если книга похожа только поверхностно, не включай её.
12. Лучше предложить меньше спорных вариантов, чем выдумать неправильные данные.
13. Ответ должен быть строго валидным JSON.
14. Не добавляй markdown, комментарии или пояснения вне JSON.
15. Обязательно закрывай все фигурные и квадратные скобки JSON.
16. Не обрывай JSON.
17. Перед отправкой ответа проверь, что JSON можно распарсить через json.loads().
18. Не используй trailing comma после последнего элемента массива.

Требования к reason:
- reason должен быть подробным: 2–3 предложения.
- Объясни, чем именно книга похожа на исходную.
- Укажи не только жанр, но и конкретные признаки сходства: темы, атмосфера, стиль, конфликт, тип героя, настроение или структура сюжета.
- Не используй общие фразы вроде "похожа по атмосфере" без объяснения.
- Не пересказывай весь сюжет.

Верни {candidate_count} кандидатов, чтобы система могла проверить их через книжные метаданные и отфильтровать ошибочные варианты.

Формат ответа строго такой:
{{
  "recommendations": [
    {{
      "title": "Конкретное название книги",
      "author": "Точный автор книги",
      "reason": "Подробное объяснение в 2–3 предложениях, почему эта книга подходит к исходной.",
      "similarity_type": "{similarity_type}",
      "confidence": 0.9,
      "search_query": "Название книги Автор"
    }}
  ]
}}

Требования к полям:
- title: строка, конкретная существующая книга.
- author: строка, настоящий автор книги.
- reason: строка, 2–3 предложения.
- similarity_type: только "{similarity_type}".
- confidence: число от 0 до 1.
- search_query: строка для поиска книги в Google Books или Open Library в формате "Название Автор".

Верни только JSON.
"""